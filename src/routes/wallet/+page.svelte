<script lang="ts">
	// Import necessary libraries
	import { onMount, onDestroy } from 'svelte';
	import Chart from 'chart.js/auto';
	import html2canvas from 'html2canvas';
	import Icon from '@iconify/svelte';
	import { goto } from '$app/navigation';
	import PortfolioRevealModal from '$lib/components/PortfolioRevealModal.svelte';

	// Exported variables and initial data
	export let data;

	// Countdown components state
	let nextUpdateTimeLeft = 0;
	let autoRedeemTimeLeft = 0;
	let interval: NodeJS.Timeout;

	// Canvas and Chart variables
	let canvas: HTMLCanvasElement;
	let chart: Chart;

	// Reactive declarations using server data
	$: portfolioData = data.portfolioData;
	$: networthHistory = portfolioData?.netWorthHistory || [];
	$: netWorthChange = getNetWorthChange(networthHistory);
	$: statusMessage = getStatusMessage(netWorthChange);
	$: isPositive = netWorthChange > 0;
	$: currentNetWorth = networthHistory?.length
		? (networthHistory[networthHistory.length - 1]?.netWorth ?? 0)
		: 0;

	let showRevealModal = false;

	// Lifecycle hooks
	onMount(() => {
		updateCountdowns();
		interval = setInterval(updateCountdowns, 1000);

		const ctx = canvas.getContext('2d');
		if (ctx) {
			chart = new Chart(ctx, {
				type: 'line',
				data: {
					labels: Array.from({ length: networthHistory.length }, (_, i) => `Hour ${i + 1}`),
					datasets: [
						{
							label: 'Net Worth',
							data: networthHistory.map((h) => h.netWorth),
							borderColor: '#6c5ce7',
							backgroundColor: 'rgba(108, 92, 231, 0.1)',
							borderWidth: 3,
							tension: 0.4,
							fill: true,
							pointBackgroundColor: '#a55eea',
							pointBorderColor: '#fff',
							pointHoverRadius: 8,
							pointHoverBackgroundColor: '#ff6b6b'
						}
					]
				},
				options: {
					responsive: true,
					animation: {
						duration: 2000,
						easing: 'easeInOutQuart'
					},
					plugins: {
						legend: {
							display: false
						}
					},
					scales: {
						y: {
							grid: {
								color: 'rgba(255, 255, 255, 0.1)'
							},
							ticks: {
								color: '#fff',
								font: {
									family: 'VT323'
								}
							}
						},
						x: {
							grid: {
								color: 'rgba(255, 255, 255, 0.1)'
							},
							ticks: {
								color: '#fff',
								font: {
									family: 'VT323'
								}
							}
						}
					}
				}
			});
		}
	});

	onDestroy(() => {
		if (interval) clearInterval(interval);
		if (chart) chart.destroy();
	});

	// Functions
	function updateCountdowns() {
		nextUpdateTimeLeft = calculateTimeLeft(portfolioData.nextCheckTime);
		autoRedeemTimeLeft = calculateTimeLeft(portfolioData.redeemTime);
	}

	function calculateTimeLeft(targetTime: number) {
		const now = new Date().getTime();
		const difference = targetTime - now;
		return Math.max(0, Math.floor(difference / 1000));
	}

	function formatTime(seconds: number) {
		const hours = Math.floor(seconds / 3600);
		const minutes = Math.floor((seconds % 3600) / 60);
		const remainingSeconds = seconds % 60;

		return `${String(hours).padStart(2, '0')}:${String(minutes).padStart(
			2,
			'0'
		)}:${String(remainingSeconds).padStart(2, '0')}`;
	}

	function getNetWorthChange(history: Array<{ netWorth: number; coinsWorth: object }>) {
		if (!history?.length || history.length < 2) return 0;
		const latest = history[history.length - 1]?.netWorth ?? 0;
		const previous = history[history.length - 2]?.netWorth ?? 0;
		return latest - previous;
	}

	function getStatusMessage(change: number) {
		if (change > 50) {
			return 'Absolutely crushing it! üöÄ';
		} else if (change > 25) {
			return 'Looking good! üìà';
		} else if (change > 0) {
			return 'Slow and steady wins the race! üê¢';
		} else {
			return 'HODL strong! üíé';
		}
	}

	async function sharePortfolio(portfolioId: string) {
		try {
			// Capture the portfolio section
			const element = document.querySelector('.max-w-4xl');
			if (!element) throw new Error('Portfolio element not found');

			const canvas = await html2canvas(element as HTMLElement, {
				backgroundColor: '#000', // Match your background
				scale: 2 // Higher quality
			});

			// Convert canvas to blob
			const blob = await new Promise<Blob>((resolve) => {
				canvas.toBlob((blob) => {
					resolve(blob!);
				}, 'image/png');
			});

			// Download the image
			const link = document.createElement('a');
			link.href = URL.createObjectURL(blob);
			link.download = 'portfolio.png';
			link.click();
			URL.revokeObjectURL(link.href);
		} catch (error) {
			console.error('Error sharing portfolio:', error);
			alert('Failed to download portfolio. Please try again.');
		}
	}

	function handleBack() {
		goto('/');
	}
</script>

<svelte:head>
	<title>Portfolio Dashboard üöÄ</title>
	<meta name="description" content="Your crypto portfolio tracker" />
</svelte:head>

<div class="meme-bg min-h-screen p-4 text-white">
	<div class="mx-auto max-w-4xl">
		<!-- Back Button and Logo -->
		<div class="mb-8 flex items-center justify-between">
			<button
				on:click={handleBack}
				class="cyber-card group flex items-center gap-2 rounded-xl p-3 transition-all duration-300 hover:scale-105"
			>
				<Icon
					icon="lucide:arrow-left"
					class="h-5 w-5 text-purple-400 group-hover:text-purple-300"
				/>
				<span class="font-vt323 hidden text-lg sm:inline">BACK</span>
			</button>
		</div>

		<!-- Header -->
		<h1 class="glitch-text mb-8 text-center text-2xl md:text-4xl">‚ú® Portfolio Dashboard ‚ú®</h1>

		<!-- Portfolio Details -->
		<div class="cyber-card mb-8 rounded-xl p-6">
			<div>
				<div class="mb-8 flex items-center justify-between">
					<h2 class="font-vt323 text-3xl">üí∞ BAG STATUS</h2>
					{#if currentNetWorth !== null}
						<p class="font-press-start-2p text-2xl text-green-400">
							${currentNetWorth.toLocaleString()}
						</p>
					{:else}
						<div class="text-center">
							<p class="font-press-start-2p text-xl text-purple-400">Waiting for first update...</p>
						</div>
					{/if}
				</div>

				<div class="space-y-4">
					{#each portfolioData?.coins ?? [] as coin, index}
						<div class="cyber-card rounded-lg p-4 transition-transform hover:scale-105">
							<div class="flex items-center justify-between">
								<div>
									<p class="font-vt323 text-xl text-purple-400">
										Mystery Coin #{index + 1} ü§´
									</p>
									<p class="font-press-start-2p text-sm text-gray-400">
										{(coin?.amount ?? 0).toLocaleString()} coins
									</p>
								</div>
								<p class="font-press-start-2p text-lg text-green-400">
									{#if portfolioData?.netWorthHistory?.length > 0}
										${(
											portfolioData.netWorthHistory[portfolioData.netWorthHistory.length - 1]
												?.coinsWorth?.[coin.id] ?? 0
										).toLocaleString()}
									{:else}
										$???
									{/if}
								</p>
							</div>
						</div>
					{/each}
				</div>
			</div>
		</div>

		{#if !data.user.hasRedeemed}
			<!-- Countdown Timers -->
			<div class="mb-8 grid grid-cols-1 gap-6 md:grid-cols-2">
				<!-- Next Update Countdown -->
				<div class="cyber-card rounded-xl p-6 text-center">
					<h3 class="font-vt323 mb-2 text-xl">‚è∞ NEXT UPDATE IN</h3>
					<div class="relative">
						<div class="cyber-card overflow-hidden rounded-xl p-4 text-center">
							<div
								class="animate-pulse-slow absolute inset-0 bg-gradient-to-r from-purple-500/10 to-pink-500/10"
							></div>
							<p class="font-vt323 glitch-text text-4xl tracking-wider">
								{formatTime(nextUpdateTimeLeft)}
							</p>
						</div>
					</div>
				</div>

				<!-- Auto Redeem Countdown -->
				<div class="cyber-card rounded-xl p-6 text-center">
					<h3 class="font-vt323 mb-2 text-xl">‚ö° AUTO-REDEEM IN</h3>
					<div class="relative">
						<div class="cyber-card overflow-hidden rounded-xl p-4 text-center">
							<div
								class="animate-pulse-slow absolute inset-0 bg-gradient-to-r from-purple-500/10 to-pink-500/10"
							></div>
							<p class="font-vt323 glitch-text text-4xl tracking-wider">
								{formatTime(autoRedeemTimeLeft)}
							</p>
						</div>
					</div>
				</div>
			</div>
		{/if}

		<!-- Networth Graph -->
		{#if networthHistory.length > 0}
			<div class="cyber-card mb-6 rounded-xl p-4">
				<canvas bind:this={canvas}></canvas>
			</div>
		{:else}
			<div class="cyber-card mb-6 rounded-xl p-8 text-center">
				<p class="font-vt323 mb-4 text-2xl">üìä Waiting for first update...</p>
			</div>
		{/if}

		<!-- Status Message -->
		{#if currentNetWorth !== null}
			<div class="cyber-card relative mb-6 overflow-hidden rounded-xl p-4 text-center">
				<div
					class="animate-pulse-slow absolute inset-0 opacity-10"
					style="background: {isPositive ? 'var(--gradient-1)' : 'var(--gradient-2)'}"
				></div>
				<p class="font-press-start-2p mb-2 text-sm">STATUS UPDATE:</p>
				<p class="font-vt323 animate-bounce-slow text-2xl">{statusMessage}</p>
				<p
					class="font-press-start-2p mt-4 text-sm"
					class:text-green-400={isPositive}
					class:text-red-400={!isPositive}
				>
					{isPositive ? '‚ÜóÔ∏è' : '‚ÜòÔ∏è'} ${Math.abs(netWorthChange).toLocaleString()}
				</p>
			</div>
		{/if}

		<!-- Action Buttons -->
		<div class="mb-20 grid grid-cols-1 gap-4 md:grid-cols-2">
			<button
				on:click={() => sharePortfolio(portfolioData.id)}
				class="cyber-card font-press-start-2p w-full rounded-xl px-6 py-4 text-sm transition-all hover:text-purple-400"
			>
				üîó SHARE THE SAUCE
			</button>

			{#if !data.user.hasRedeemed}
				<button
					on:click={() => (showRevealModal = true)}
					class="cyber-card font-press-start-2p w-full rounded-xl px-6 py-4 text-sm transition-all hover:text-green-400"
				>
					üí∏ CASH OUT
				</button>
			{/if}
		</div>
	</div>
</div>

<PortfolioRevealModal
	show={showRevealModal}
	onClose={() => (showRevealModal = false)}
	portfolio={portfolioData?.coins || []}
	netWorthHistory={networthHistory}
	coins={data.coins}
	clerkId={data.user.clerkId}
/>

{#if data.user.hasRedeemed}
  <div class="cyber-window mx-auto mb-8 max-w-xl">
    <div class="window-header bg-red-600">
      <div class="window-dots">
        <div class="dot"></div>
